    private void btnenviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnenviarActionPerformed
        // TODO add your handling code here:
        
         if(txtrespuesta.getText().length() == 0){
        
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar la respuesta a la pregunta de seguridad");
            txtrespuesta.requestFocus();
            return;
        } 
        
        
        if(txtsecreta.toString()!=txtrespuesta.toString())
        {
            JOptionPane.showMessageDialog(rootPane,"La contraseña es: " + txtcontraseña.getText() ,"Recuperacion de Contraseña", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
        }
        else
        {
            JOptionPane.showMessageDialog(rootPane,"Respuesta Invalida, se cerrará la ventana", "Recuperacion de Contraseña", JOptionPane.ERROR_MESSAGE);
            this.dispose();
        }
    }//GEN-LAST:event_btnenviarActionPerformed

