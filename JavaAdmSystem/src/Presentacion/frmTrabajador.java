    private void tablalistadoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablalistadoMouseClicked

        btnGuardar.setText("Editar");
        habilitar();
        pwdConfirmacionTrabajador.setEnabled(false);
        
        
        
        btnEliminar.setEnabled(true);
        
        accion="Editar";
        
        //esto es para pasar los datos de la tabla a los txtbox del form
        
        int fila = tablalistado.rowAtPoint(evt.getPoint());
        

        String NumCi = tablalistado.getValueAt(fila,0).toString();
        

        
        txtCedulaTrabajador.setText(NumCi.substring(2)); //se le pone el substring para eliminar el V- o el E- de la ci

        txtCedulaTrabajador.setEnabled(false);
        cbCi.setEnabled(false);

        String firstLetter  = "";
        
        firstLetter = String.valueOf(NumCi.charAt(0)); //evalua la primera letra de la ci
        
        if (firstLetter.equals("E")){ //para ver si comienza con E o con V
        
            cbCi.setSelectedItem("E-");
        }
        if (firstLetter.equals("V")){ //para ver si comienza con V
        
            cbCi.setSelectedItem("V-");
        }        
        
        
        txtCedulaTrabajador.setEnabled(false);
        
        //vuelve la confirmacion falsa
        pwdConfirmacionTrabajador.setEnabled(false);
        jLabel8.setEnabled(false);

        
        txtNombreTrabajador.setText(tablalistado.getValueAt(fila,1).toString());
        txtDireccionTrabajador.setText(tablalistado.getValueAt(fila,2).toString());        
        cbSexo.setSelectedItem(tablalistado.getValueAt(fila,3).toString());
        cbAcceso.setSelectedItem(tablalistado.getValueAt(fila,4).toString());
        pwdContraseñaTrabajador.setText(tablalistado.getValueAt(fila,5).toString()); 
        txtTelefonoTrabajador.setText(tablalistado.getValueAt(fila,6).toString()); 
        txtCorreoTrabajador.setText(tablalistado.getValueAt(fila,7).toString()); 
        cbPreguntaSeguridad.setSelectedItem(tablalistado.getValueAt(fila,8).toString());
        txtRespuesta.setText(tablalistado.getValueAt(fila,9).toString()); 

        
             
    }//GEN-LAST:event_tablalistadoMouseClicked

