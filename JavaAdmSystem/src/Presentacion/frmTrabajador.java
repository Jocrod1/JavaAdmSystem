    public static boolean valEmail(String input){
        String emailRegex = "^[A-Z0-9._%+-]+@[A-Z0-9.-]+\\.[A-Z]{2,6}$";
        Pattern emailPat = Pattern.compile(emailRegex,Pattern.CASE_INSENSITIVE);
        Matcher matcher = emailPat.matcher(input);
        return matcher.find();
    }
    
    
    
    
    
    
    
    
    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        //valida si los campos estan vacios
        
        if(txtCedulaTrabajador.getText().length() == 0){
        
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar la cedula del trabajador");
            txtCedulaTrabajador.requestFocus();
            return;
        }
        
        if(txtNombreTrabajador.getText().length() == 0){
        
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar el nombre del trabajador");
            txtNombreTrabajador.requestFocus();
            return;
        }
        
        
        if(txtRespuesta.getText().length() == 0){
        
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar la respuesta a la pregunta de seguridad");
            txtRespuesta.requestFocus();
            return;
        }        
        
        if(txtDireccionTrabajador.getText().length() == 0){
        
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar la direccion del trabajador");
            txtDireccionTrabajador.requestFocus();
            return;
        }
        
        if(pwdContraseñaTrabajador.getPassword().length == 0){
        
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar la contraseña del trabajador");
            pwdContraseñaTrabajador.requestFocus();
            return;
        }
        
        
        //valida si la ci tiene letras
        if ( !(txtCedulaTrabajador.getText().matches("[0-9]+")) ){
        JOptionPane.showMessageDialog(rootPane, "La cedula solo puede contener numeros");
        txtCedulaTrabajador.requestFocus();
        return;
        }
        
        //valida si el tlfno tiene letras
        if ( !(txtTelefonoTrabajador.getText().matches("[0-9]+")) ){
        JOptionPane.showMessageDialog(rootPane, "El numero de telefono solo puede contener numeros");
        txtTelefonoTrabajador.requestFocus();
        return;
        }
        
        
        //valida si el nombre tiene numeros
        if ( !(Pattern.matches("[a-zA-Z\\s ]+", txtNombreTrabajador.getText()) ) ){
        JOptionPane.showMessageDialog(rootPane, "El nombre solo puede contener letras");
        txtNombreTrabajador.requestFocus();
        return;
        }        
        
        
        //valida el formato del correo    
            
        String email = txtCorreoTrabajador.getText();
        
        if (!valEmail(email)){  
          JOptionPane.showMessageDialog(rootPane, "El correo no es valido, vuelva a escribirlo");
          txtCorreoTrabajador.requestFocus();
          return;
        }
        
        
        
        
        
        
        
        
        if(btnGuardar.getText().equals("Guardar")){  //esto valida si el boton es de guardar o editar
            
        
        if(pwdConfirmacionTrabajador.getPassword().length == 0){
        
            JOptionPane.showMessageDialog(rootPane, "Debes ingresar la contraseña de nuevo");
            txtDireccionTrabajador.requestFocus();
            return;
            
            
        }
        
        
        //confirma que las contraseñas sean iguales
        
        if ( !(Arrays.equals(pwdContraseñaTrabajador.getPassword(), pwdConfirmacionTrabajador.getPassword())) ) {
            JOptionPane.showMessageDialog(rootPane, "La confirmacion de la contraseña no coincide. Porfavor vuelva a escribirla");
            pwdConfirmacionTrabajador.requestFocus();
            return;
        } 
        
        //cedula unica
        try {
            DefaultTableModel modelo;
            ftrabajador func= new ftrabajador();
            vtrabajador dts= new vtrabajador();
            
            
            
            String TipoCi = cbCi.getSelectedItem().toString();
            String NumCi = txtCedulaTrabajador.getText();
            
            dts.setId_trabajador(TipoCi+NumCi);
            
            modelo=func.usuariorepetido(dts.getId_trabajador());
            

            
            tablarepetida.setModel(modelo);
            
            if(func.totalregistros>0)
            {
                JOptionPane.showMessageDialog(rootPane,"Cedula ya Registrada!","Error", JOptionPane.ERROR_MESSAGE);
                
                txtCedulaTrabajador.requestFocus();
                
                return;
                
            }
            
            
        } catch (Exception e) {
        }
        
        
        
        
        
        
        }
        
        vtrabajador dts = new vtrabajador();
        ftrabajador func = new ftrabajador();
        
        String TipoCi = cbCi.getSelectedItem().toString();
        String NumCi = txtCedulaTrabajador.getText();
        
        dts.setId_trabajador(TipoCi+NumCi);
        dts.setNombre(txtNombreTrabajador.getText());
        dts.setDireccion(txtDireccionTrabajador.getText());
        dts.setTelefono(txtTelefonoTrabajador.getText());
        dts.setCorreo(txtCorreoTrabajador.getText());
        String a = new String(pwdContraseñaTrabajador.getPassword());
        dts.setContraseña(a);
        dts.setRespuesta(txtRespuesta.getText());
        int seleccionado_sexo = cbSexo.getSelectedIndex();
        dts.setSexo((String) cbSexo.getItemAt(seleccionado_sexo) );
  
        int seleccionado_acceso = cbAcceso.getSelectedIndex();
        dts.setAcceso((String) cbAcceso.getItemAt(seleccionado_acceso) );
        
        int seleccionado_pregunta = cbPreguntaSeguridad.getSelectedIndex();
        dts.setPregunta((String) cbPreguntaSeguridad.getItemAt(seleccionado_pregunta) );
        
        
        if(accion.equals("Guardar")){
                      
            
            if (func.insertar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "Se guardo correctamente");
                mostrar("");
                limpiar();
                inhabilitar();
            }
        }else if(accion.equals("Editar")){
            
            dts.setId_trabajador(cbCi.getSelectedItem()+txtCedulaTrabajador.getText());
            
            if (func.editar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "El trabajador fue editado exitosamente");
                mostrar("");
                limpiar();
                inhabilitar();
                
            }
        
        }
  
        
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed

        
        String TipoCi = cbCi.getSelectedItem().toString();
        String NumCi = txtCedulaTrabajador.getText();
        
        String Cedula = TipoCi+NumCi;
        
        if (!txtCedulaTrabajador.getText().equals("")) {
            int confirmacion = JOptionPane.showConfirmDialog(rootPane,"Estas seguro de eliminar este trabajador?");
            
            if (confirmacion==0) {  //significa que SI desea que se elimine
                ftrabajador func = new ftrabajador();
                vtrabajador dts = new vtrabajador();
                
                dts.setId_trabajador(Cedula);
                func.eliminar(dts); //se envian los datos para la elimacion, osea solo la id
                mostrar(""); //se envia una cadena en blanco para que se muestren todos
                limpiar();
                inhabilitar();
                
                
                
                
            }
            
        }
        
    }//GEN-LAST:event_btnEliminarActionPerformed

